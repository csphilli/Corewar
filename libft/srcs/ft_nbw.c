int		ft_nbw(const char *s, char c)
{
	int	nbr;

	nbr = 0;
	if (*s != c && *s)
	{
		s++;
		nbr++;
	}
	while (*s)
	{
		while (*s == c)
		{
			s++;
			if (*s != c && *s)
				nbr++;
		}
		s++;
	}
	return (nbr);
}
