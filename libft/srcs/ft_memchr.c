#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	c1;
	unsigned char	*ptr_s;
	size_t			i;

	ptr_s = (unsigned char *)s;
	i = 0;
	c1 = (unsigned char)c;
	while (i < n)
	{
		if (ptr_s[i] == c1)
			return (&ptr_s[i]);
		i++;
	}
	return (NULL);
}
